from turtle import*
fillcolor('brown')
begin_fill()
setheading(0)
forward(5)
left(90)
forward(120)
left(90)
forward(5)
left(90)
forward(120)
left(90)
forward(5)
end_fill()

penup()
goto(xcor()-5,ycor()+120)
setheading(180)
fillcolor('black')
begin_fill()
pendown()
circle(40,extent=45)
circle(-40,extent=45)
setheading(270)
forward(50)
setheading(180)
circle(-40,extent=-45)
circle(40,extent=-45)
end_fill()

penup()
setheading(180)
forward(30)
right(90)
